public class Attributes{
        	private int hits;
        	private int length;
        	
        	Attributes(int hits,int length){
        	this.hits = hits;
        	this.length = length;
        	}
        	
        	public int getHits(){
        	return this.hits;
        	}
        	
        	public int getLength(){
        	return this.length;
          }
        }
